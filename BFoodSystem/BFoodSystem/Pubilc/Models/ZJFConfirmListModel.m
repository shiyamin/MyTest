#import "ZJFConfirmListModel.h"

@implementation ZJFConfirmListModel


- (void)setValue:(id)value forUndefinedKey:(NSString *)key {

    if ([key isEqualToString:@"id"]) {
        self.dishesID = value;
    }
    
    if ([key isEqualToString:@"sku_id"]) {
        self.skuId = value;
    }
    
    if ([key isEqualToString:@"type_id"]) {
        self.typeId = value;
    }
    
    if ([key isEqualToString:@"pic_list"]) {
        self.picList = value;
    }
    
    if ([key isEqualToString:@"type_name"]) {
        self.typeName = value;
    }
    
    if ([key isEqualToString:@"pic_url"]) {
        self.picUrlArr = value;
    }
}

@end
